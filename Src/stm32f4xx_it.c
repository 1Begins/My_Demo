/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */
  uint32_t buff_length;//接收数据长度

  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);
  /* USER CODE BEGIN USART6_IRQn 1 */

	 if((__HAL_UART_GET_FLAG(&huart6,UART_FLAG_IDLE))!= RESET)//检测到DMA线路空闲
	{
		HAL_UART_DMAStop(&huart6);//关闭DMA接收
		__HAL_UART_CLEAR_IDLEFLAG(&huart6);//通过读取SR,DR寄存器去清零,使其退出串Usart3_Clean_IDLE_Flag口中断。
		buff_length=JUD_RC_BUFFER_SIZE-(__HAL_DMA_GET_COUNTER(&hdma_usart6_rx));//读取DMA剩余传输量
		USART_RX_STA=buff_length;//获取数据长度
		JudgeRead(Jud_Rx_Buffer);//裁判系统接收
		memset(Jud_Rx_Buffer,0,100);
		HAL_UART_Receive_DMA(&huart6,Jud_Rx_Buffer,JUD_RC_BUFFER_SIZE);//重启DMA接收
	}

  /* USER CODE END USART6_IRQn 1 */
}

